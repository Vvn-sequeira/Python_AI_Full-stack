
# Set ( Mathamatical )

set1 = {1,2,3,45,6}
set2 = {45}

Union_set = set1 | set2 
print(f"Union set is : {Union_set}")

Intersection_set = set1 & set2 
print(f"Intersection : {Intersection_set}")

only_from_set1 = set1 - set2 
print(f"Only from essential : {only_from_set1}")


