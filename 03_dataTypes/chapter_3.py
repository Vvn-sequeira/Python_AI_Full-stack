# Integer Data type

black_tea = 14 
ginger_gram = 3 

total_gram = black_tea + ginger_gram
print(f"Total grams id  : {total_gram}")

Remaining_gram = black_tea - ginger_gram
print(f"Remaining grams id  : {Remaining_gram}")

Soda_can = 12 
servings = 5
Soda_per_serving = Soda_can / servings; # Normal Division
print(f"Soda per serving :{ Soda_per_serving}")


Soda_per_serving_int = Soda_can // servings;  # Floor Division
print(f"Soda per serving int :{ Soda_per_serving_int}")

Soda_per_serving_mod = Soda_can % servings; # Reminder 
print(f"Soda per serving mod :{ Soda_per_serving_mod}") 

Soda_per_serving_exp = Soda_can ** servings; # Exponent
print(f"Soda per serving exp :{ Soda_per_serving_exp}")

Number_readable = 1_000_00_000 ;
print(f"Number readable : {Number_readable}")

